<h1>Cosmic Origins Program Analysis Group<br>
Events: Meetings</h1>


<h2><a href="https://aas.org/meetings/aas227" title="227th meeting of the American Astronomical Society" target="_blank">227th meeting of the American Astronomical Society</a></h2>
<p>Kissimmee, Florida<br>
4 &#8211; 8 January 2016</p>

<hr>

<h3>COPAG Meeting</h3>

<p class="spaced-bottom">Monday, 4 January 2016<br>
Kissimmee, FL<br>
3:00pm &#8211; 5:00pm<br>
Room: Osceola A</p>

<details class="agenda" open><summary><h4>Agenda</h4></summary>

<table>
    <tr>
        <th width="13%">Time</th>
        <th>Presentation</th>
    </tr>
    <tr>
        <td rowspan="2">3:10pm</td>
        <td>Paul Scowen<br>
        <strong>Introduction and reasoning behind Call for Probe White Papers</strong> [<a href="/copag/meetings/AAS_Jan2016/presentations/Scowen_Intro.pdf" target="_blank">PDF</a>]</td>
    </tr>
    <tr>
        <td>This will be a simple overview of the landscape we will have heard from Paul Hertz earlier in the day and the motivation the COPAG Executive Committee committed to in December to initiate a discussion within the Cosmic Origins community on the subject of Probes since the focus has been on Flagships for the last 12 months.  In comparison with the Physics of the Cosmos and the Exoplanet communities, not much emphasis has been placed on possible Probe-class mission concepts. We'd like to change that.</td>
    </tr>

    <tr>
        <td colspan="2" align="left"><strong>Probe Concepts</strong></td>
    </tr>

    <tr>
        <td rowspan="2">3:30pm</td>
        <td>Sally Heap &amp; Tony Hull<br>
        <strong>The Galaxy Evolution Spectroscopic Probe (GESP)</strong> [<a href="/copag/meetings/AAS_Jan2016/presentations/Heap_Probe_GESP.pdf" target="_blank">PDF</a>]</td>
    </tr>
    <tr>
        <td>GESP is a multi-object slit spectrograph designed to help understand galaxy evolution in a critical era in the history of the universe: the period when star formation peaked and started its decline which continues to the present day. To isolate the various processes driving the evolution of these galaxies, GESE will obtain rest-frame far-UV spectra of >50,000 galaxies at redshifts, z~1-2. To obtain such a large number of spectra, multiplexing over a wide field is an absolute necessity. A digital micro-mirror device (DMD) offers an innovative solution to this challenge. By forming small "slits" around target galaxies in the field, a DMD enables spectroscopy of hundreds of sources in a single exposure while eliminating overlapping spectra of other sources and blocking unwanted background like zodiacal light. The actual multiplexing factor in the GESP case is a function of the field of view and the limiting sensitivity of the spectrograph. We find that a 1m-class space telescope combined with a custom orbit enabling a high fraction of time on target and uninterrupted long exposures (~10 hr) are optimal for this spectroscopic survey program. We expect the cost of GESP to be similar to that of a NASA Discovery mission.</td>
    </tr>

    <tr>
        <td rowspan="2">3:40pm</td>
        <td>Stephen Rinehart<br>
        <strong>The Space High Angular Resolution Probe for the Infrared (SHARPIR)</strong></td>
    </tr>
    <tr>
        <td>SHARPIR is a small (~12-meter) baseline far-infrared interferometer being developed to fit within the Probe mission category.  By operating between 20-160 microns, the angular resolution achieved with SHARPIR would be complement observations with JWST, providing valuable new data for studies of star formation.  At the same point, SHARPIR would serve as a stepping stone to the large interferometry missions envisioned within the Astrophysics Roadmap.</td>
    </tr>

    <tr>
        <td>3:50pm</td>
        <td>Mark Bradford<br>
        <strong>Notion for a Probe-class Cryogenic Far-IR Mission</strong></td>
    </tr>

    <tr>
        <td rowspan="2">4:00pm</td>
        <td>Joseph Lazio<br>
        <strong>Tracking the Evolution of the Neutral Intergalactic Medium</strong> [<a href="/copag/meetings/AAS_Jan2016/presentations/COPAG_ProbeClass-CosmicDawn.pdf" target="_blank">PDF</a>]</td>
    </tr>
    <tr>
        <td>The highly redshifted hyperfine line from the neutral hydrogen provides a powerful probe of the evolution of the intergalactic medium prior to and during the formation of the first luminous objects. I describe concepts for the next step in detecting and studying the hyperfine hydrogen line from space.</td>
    </tr>

    <tr>
        <td rowspan="2">4:10pm</td>
        <td>Paul Scowen<br>
        <strong>High-ORbit Ultraviolet-visible Satellite &#8211; (HORUS)</strong> [<a href="/copag/meetings/AAS_Jan2016/presentations/HORUS_SALSO_MSFC.pdf" target="_blank">PDF</a>]</td>
    </tr>
    <tr>
        <td>This mission could assemble a comprehensive UV/Optical imaging survey of Galactic and Magellanic Clouds star formation to probe many aspects of the star-formation process.  Primary goal is to understand the evolution of circumstellar protoplanetary disks and other detailed aspects of star formation, in a wide variety of environments.  Do this with a comprehensive emission-line survey of nearby massive star-forming regions in the Milky Way.  HORUS will also study shocks in jets and outflows from young stars, including proper motions of jets and shocks, as well as systematically map the detailed excitation structure of HII regions, stellar winds, supernova remnants and supershells/superbubbles.  Requires 40 milliarcsec resolution stable PSF, 200 - 1075nm wavelength coverage, 15' x 15' imaging and R ~ 40,000 spectroscopy.</td>
    </tr>

    <tr>
        <td rowspan="2">4:20pm</td>
        <td>Paul Scowen<br>
        <strong>MidEx concept that would benefit greatly from higher budget cap &#8211; ORION</strong> [<a href="/copag/meetings/AAS_Jan2016/presentations/Orion_Overview-2.pdf" target="_blank">PDF</a>]</td>
    </tr>
    <tr>
        <td>Build the first UV/Optical widefield, high resolution database of tar formation and its products within nearby massive stellar environments. This can be used to determine how often stars and solar systems form and survive.  Monitor protostars to understand the range of formation and evolutionary modes that are allowed by the process of star formation.  What are the fundamental processes that govern the assembly of baryonic matter into stars and planets.  Explore every type of environment from nearby low-mass stars to extreme environments that produce the most massive stars (ULIRGS). Determine how stellar properties (disk lifetimes, starspot activity, rotation periods, accretion rates, outflow rates...) vary with age and stellar mass. Need wide field <em>and</em> diffraction-limited performance extending to 200nm or shorter wavelength.</td>
    </tr>

    <tr>
        <td>&nbsp;</td>
        <td>Asantha Cooray<br>
        <strong>Cosmic Dawn Intensity Mapper</strong> [<a href="/copag/meetings/AAS_Jan2016/presentations/Cooray-COPAG-Probe.pdf" target="_blank">PDF</a>]</td>
    </tr>

    <tr>
        <td>4:30pm</td>
        <td>Other Probe concepts (walk ups) or Continued Discussion</td>
    </tr>

    <tr>
        <td>4:45pm</td>
        <td>Future steps by COPAG</td>
    </tr>

    <tr>
        <td>5:00pm</td>
        <td>Adjourn</td>
    </tr>

