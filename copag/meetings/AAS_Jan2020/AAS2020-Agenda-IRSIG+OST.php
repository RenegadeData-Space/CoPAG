<h2><a href="https://aas.org/meetings/aas235">235th Meeting of the American Astronomical Society</a></h2>
<p>Honolulu, Hawaii<br>
4 &#8211; 8 January 2020</p>

<hr>

<h3>COPAG Infrared Science Interest Group (IR SIG) + Origins Space Telescope (OST)</h3>
<p>Tuesday, 7 January 2020<br>
9:30am &#8211; 11:30am<br>
Room 304AB Convention Center</p>

<h4>IR SIG Splinter Session: The Role of Infrared Astronomy in NASA's Strategic Vision to 2030</h4>

<p>All are welcome to join the IR SIG splinter session to hear talks on current and future missions under the IR SIG purview as well as a panel discussion focussed on the role of infrared observations over the coming decade. Confirmed invited speakers include Phil Korngut (SPHEREx), Paul Goldsmith (ASTHROS), Tiffany Kataria (Origins), Hal Yorke (SOFIA) and Matt Bradford (SPICA).</p> 

<p>More details on the IR SIG and the AAS activities are found here: <a href="https://www.dropbox.com/s/imen999xdsnefvd/AAS_flyer_v1.pdf?" target="_blank">PDF</a></p>

